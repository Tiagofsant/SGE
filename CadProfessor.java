    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Professor prof = new Professor();
        ProfessorDao dprof = new ProfessorDao();
        Utilitario util = new Utilitario();

        prof.setNome_professor(this.Jt_nome_prof.getText());
        prof.setSobrenome_professor(this.Jt_sobrenome_prof.getText());
        prof.setRg_professor(Integer.parseInt(this.Jt_rg_prof.getText()));
        prof.setCpf_professor(Double.parseDouble(this.Jt_cpf_prof.getText()));
        prof.setEndereco_professor(this.Jt_endereco_prof.getText());
        prof.setTelefone_professor(Double.parseDouble(this.Jt_telefone_prof.getText()));
        prof.setEmail_professor(this.Jt_email_prof.getText());
        prof.setCadastro_professor(new java.sql.Date(jCadastro.getDate().getTime()));
        prof.setNascimento_professor(new java.sql.Date(jNascimento.getDate().getTime()));

        dprof.inserir(prof);
    }//GEN-LAST:event_jButton2ActionPerformed

