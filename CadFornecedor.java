    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        Fornecedor forn = new Fornecedor();
        FornecedorDao fornd = new FornecedorDao();
        Utilitario util = new Utilitario();

        forn.setNome_fornecedor(this.Jt_nome_fornecedor.getText());
        forn.setCpfcnpj_fornecedor(Double.parseDouble(this.Jt_cnpj_fornecedor.getText()));
        forn.setEndereco_fornecedor(this.Jt_endereco_fornecedor.getText());
        forn.setTelefone_fornecedor(Double.parseDouble(this.Jt_telefone_fornecedor.getText()));
        forn.setEmail_fornecedor(this.Jt_email_fornecedor.getText());
        forn.setCadastro_fornecedor(new java.sql.Date(jCadastro.getDate().getTime()));
        forn.setEntrega_fornecedor(new java.sql.Date(jEntrega.getDate().getTime()));

        fornd.inserir(forn);
    }//GEN-LAST:event_jButton2ActionPerformed

